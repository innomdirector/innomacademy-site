const countWords = (value = '') => (
  String(value)
    .replace(/\*\*/g, ' ')
    .trim()
    .split(/\s+/)
    .filter(Boolean)
    .length
)

const calculateReadingMinutes = (post) => {
  const titleWords = countWords(post.title)
  const excerptWords = countWords(post.excerpt)
  const contentWords = post.sections.reduce((sum, section) => {
    const headingWords = countWords(section.heading)
    const paragraphWords = section.paragraphs.reduce((paragraphSum, paragraph) => (
      paragraphSum + countWords(paragraph)
    ), 0)
    return sum + headingWords + paragraphWords
  }, 0)

  const totalWords = titleWords + excerptWords + contentWords
  return Math.max(6, Math.ceil(totalWords / 180))
}

export const blogPosts = BASE_BLOG_POSTS.map((post) => ({
  ...post,
  readingMinutes: calculateReadingMinutes(post),
}))

const normalizeBlogSlug = (slug = '') => (
  String(slug).trim().replace(/^\/+|\/+$/g, '')
)

const legacyBlogSlugMap = blogPosts.reduce((map, post) => {
  for (const legacySlug of post.legacySlugs || []) {
    map.set(normalizeBlogSlug(legacySlug), post.slug)
  }
  return map
}, new Map())

export const getBlogPostBySlug = (slug) => (
  blogPosts.find((post) => post.slug === normalizeBlogSlug(slug))
)

export const getCategoryLabel = (categoryId) => (
  BLOG_CATEGORIES.find((item) => item.id === categoryId)?.label || categoryId
)

export const getCanonicalBlogSlug = (slug) => {
  const normalizedSlug = normalizeBlogSlug(slug)
  if (!normalizedSlug) return ''

  if (getBlogPostBySlug(normalizedSlug)) {
    return normalizedSlug
  }

  return legacyBlogSlugMap.get(normalizedSlug) || ''
}

export const blogSlugRedirects = blogPosts.flatMap((post) => (
  (post.legacySlugs || []).map((legacySlug) => ({
    from: normalizeBlogSlug(legacySlug),
    to: post.slug,
  }))
))
